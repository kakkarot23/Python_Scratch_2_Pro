"""### 175. Remove duplicates from string."""

# Method 1: Using a set (maintains order of first appearance)
def remove_duplicates_set_ordered(s):
    seen = set()
    result = []
    for char in s:
        if char not in seen:
            seen.add(char)
            result.append(char)
    return "".join(result)

# Method 2: Using a set and join (order not guaranteed for original string)
def remove_duplicates_set_unordered(s):
    return "".join(sorted(set(s), key=s.index)) # sorted with key=s.index to maintain order

input_string = input("Enter a string: ")

print(f"Method 1 (Set Ordered): String with duplicates removed: {remove_duplicates_set_ordered(input_string)}")
print(f"Method 2 (Set Unordered - with order): String with duplicates removed: {remove_duplicates_set_unordered(input_string)}")