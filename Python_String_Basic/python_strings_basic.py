def is_palindrome_slicing(s):
    # Convert to lowercase and remove spaces for a more robust check
    cleaned_s = "".join(filter(str.isalnum, s)).lower()
    return cleaned_s == cleaned_s[::-1]

# Method 2: Using a loop
def is_palindrome_loop(s):
    cleaned_s = "".join(filter(str.isalnum, s)).lower()
    length = len(cleaned_s)
    for i in range(length // 2):
        if cleaned_s[i] != cleaned_s[length - 1 - i]:
            return False
    return True

