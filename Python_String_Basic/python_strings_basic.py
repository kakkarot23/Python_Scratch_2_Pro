def most_repeated_char_loop(s):
    if not s: # Handle empty string case
        return None
    s_filtered = s.replace(' ', '')
    if not s_filtered:
        return None

    frequency = {}
    for char in s_filtered:
        frequency[char] = frequency.get(char, 0) + 1

    if not frequency: # If string only contained spaces or was empty after filtering
        return None

    max_count = 0
    most_char = ''
    for char, count in frequency.items():
        if count > max_count:
            max_count = count
            most_char = char
    return most_char

