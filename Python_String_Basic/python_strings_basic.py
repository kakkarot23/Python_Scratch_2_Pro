from collections import Counter
def most_repeated_char_counter(s):
    if not s: # Handle empty string case
        return None
    # Filter out spaces if they shouldn't be considered
    s_filtered = s.replace(' ', '')
    if not s_filtered:
        return None
    counts = Counter(s_filtered)
    return counts.most_common(1)[0][0] # Returns the character of the most common item

