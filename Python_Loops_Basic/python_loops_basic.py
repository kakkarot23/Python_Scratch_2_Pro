"""### 56. Print all prime numbers between two numbers."""

# Get the lower and upper bounds from the user
lower = int(input("Enter lower bound of the interval: "))
upper = int(input("Enter upper bound of the interval: "))

print(f"Prime numbers between {lower} and {upper} are:")
# Iterate through each number in the interval
for num in range(lower, upper + 1):
    # Prime numbers are greater than 1
    if num > 1:
        is_prime = True
        # Check for factors from 2 up to the square root of the number
        for i in range(2, int(num**0.5) + 1):
            if (num % i) == 0:
                is_prime = False
                break
        if is_prime:
            print(num)

