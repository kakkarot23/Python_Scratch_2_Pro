"""### 59. Print Fibonacci series up to N terms."""

# Get the number of terms from the user
n_terms = int(input("Enter the number of terms for Fibonacci series: "))

# Initialize the first two terms
a, b = 0, 1
count = 0

if n_terms <= 0:
    print("Please enter a positive integer.")
elif n_terms == 1:
    print("Fibonacci series up to 1 term:")
    print(a)
else:
    print("Fibonacci series:")
    while count < n_terms:
        print(a, end=" ") # Print the current term
        nth = a + b      # Calculate the next term
        a = b            # Update 'a' to the current 'b'
        b = nth          # Update 'b' to the newly calculated next term
        count += 1
    print()

"""### 60. Print reverse Fibonacci series."""

# Get the number of terms for Fibonacci series
n_terms = int(input("Enter the number of terms for reverse Fibonacci series: "))

if n_terms <= 0:
    print("Please enter a positive integer.")
elif n_terms == 1:
    print("Reverse Fibonacci series up to 1 term:")
    print(0)
else:
    # Generate the Fibonacci series up to N terms
    fib_series = [0, 1]
    while len(fib_series) < n_terms:
        fib_series.append(fib_series[-1] + fib_series[-2])

    # Print the series in reverse order
    print("Reverse Fibonacci series:")
    for i in range(len(fib_series) - 1, -1, -1):
        print(fib_series[i], end=" ")
    print()